resultado = 2   # estamos atribuindo 2 a variavel resultado
print(resultado)

resultado += 4   # estamos atribuindo o proprio resultado + 4 ao resultado
print(resultado)

resultado -= 1   # estamos atribuindo o proprio resultado / 1 ao resultado
print(resultado)

resultado *= 3   # estamos atribuindo o proprio resultado * 3 ao resultado
print(resultado)

resultado /= 2   # estamos atribuindo o proprio resultado / 2 ao resultado
print(resultado)

resultado %= 6   # estamos atribuindo o proprio resultado % 6 ao resultado
print(resultado)

